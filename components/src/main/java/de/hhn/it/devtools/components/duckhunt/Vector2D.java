package de.hhn.it.devtools.components.duckhunt;

/**
 * Class for 2-dimensional vectors.
 */
public class Vector2D {
  private int x;
  private int y;


  public int getX() {
    return x;
  }

  public void setX(int x) {
    this.x = x;
  }

  public int getY() {
    return y;
  }

  public void setY(int y) {
    this.y = y;
  }
}
