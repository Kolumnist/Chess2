package de.hhn.it.devtools.apis.game2048;

/**
 * Enum to represent the direction the blocks get moved in
 */
public enum MovingDirection {
    /** The blocks get moved left. */
    left,
    /** The blocks get moved right.*/
    right,
    /** The blocks get moved up. */
    up,
    /** The blocks get moved down. */
    down
}