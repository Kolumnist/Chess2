package de.hhn.it.devtools.apis.ttrpgsheets;

/**
 * Enum to represent the Dice Types
 * Dice names start with a D for "Dice" followed by a number which stands for the maximum value
 * The most common Die is the D6 for example
 */
public enum DiceType {
    D2,
    D4,
    D6,
    D8,
    D10,
    D12,
    D20,
    D100
}
